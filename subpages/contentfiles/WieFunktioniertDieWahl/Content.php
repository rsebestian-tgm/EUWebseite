        <p class="lead">
            Was wird denn eigentlich gewählt?
        </p>
        <p>
            Bei der "Europawahl" wird das europäische Parlament gewählt. Das ist die Vertretung der
                EU-Bürgerinnen und -Bürger in der EU.
        </p>
        <p>
            Im Europäischen Parlament sind alle EU-Mitgliedsstaaten mit eigenen Abgeordneten vertreten.
            Wie viele Abgeordnete jedes Land hat, hängt von der Anzahl der Einwohner ab. Für diese Wahl hat Österreich
            19 Abgeordnete (vorausgesetzt, der Brexit findet rechtzeitig statt...).
        </p>

        <p class="lead">
            Wer darf aller wählen?
        </p>
        <p>
            Das Besondere bei der Europawahl ist, dass man keine österreichische Staatsbürgerschaft braucht, um in
            Österreich wählen zu können! Wenn du in Österreich wohnst und die Staatsbürgerschaft von einem
            EU-Mitgliedsstaat hast, darfst du wählen gehen!
        </p>
        <p>
            Du musst aber mindestens 16 Jahre alt sein (es zählt das Alter am Stichtag, dem 12. März 2019).
        </p>

        <p class="lead">
            Wen kann man wählen?
        </p>
        <p>
            Die Europawahl ist eine <strong>Direktwahl</strong>! Das heißt, dass man nicht Parteien wählt, sondern
            einzelne Abgeordnete. Die werden aber von Parteien aufgestellt. Momentan stehen in Österreich sechs Parteien
            zur Wahl: ÖVP, SPÖ, FPÖ, Die Grünen, NEOS und die Initiative 1Europa, die zu JETZT - Liste Peter Pilz
            gehört.
        </p>
        <p>
            Einige weitere kleine Parteien sammeln noch Unterschriften,
            um auch bei der Wahl mitzumachen - dafür braucht man nämlich die Unterstützung von einem
            EU-Parlamentsabgeordneten, drei Abgeordneten des österreichischen Nationalrates oder 2600 Unterschriften von
            Wahlberechtigten aus Österreich.
        </p>

        <p class="lead">
            Was passiert nach der Wahl?
        </p>
        <p>
            Das EU-Parlament ist in Fraktionen eingeteilt. Das sind Bündnisse zwischen Parteien, die ähnliche Positionen
            vertreten. Die meisten Parteien (und damit auch die meisten gewählten Abgeordneten) gehören zu einer
            Fraktion, manche sind aber auch fraktionslos. Diese Fraktionen könnten sich nach der Wahl ändern.
        </p>
        <p>
            Außerdem erfüllt das Parlament nach der Wahl zwei seiner Hauptaufgaben: Es wählt den Parlamentspräsidenten
            und den Präsidenten der Europäischen Kommission (die Kommission entspricht ungefähr der Regierung der EU,
            ihr Präsident ist für die EU also ungefähr das, was der Bundeskanzler für Österreich ist).
        </p>
        <p>
            Die restlichen Mitglieder der Europäischen Kommission werden vom Kommisionspräsidenten vorgeschlagen. Das
            Parlament muss diese Mitglieder dann aber noch akzeptieren.
        </p>

